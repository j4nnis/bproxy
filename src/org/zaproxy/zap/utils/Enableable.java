package org.zaproxy.zap.utils;

public abstract class Enableable {

    private boolean enabled;

    public Enableable() {
        this.enabled = false;
    }

    public Enableable(boolean enabled) {
        this.enabled = enabled;
    }

    public boolean isEnabled() {
        return enabled;
    }

    public void setEnabled(boolean enabled) {
        this.enabled = enabled;
    }

    @Override
    public int hashCode() {
        return 31 + (enabled ? 1231 : 1237);
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        Enableable other = (Enableable) obj;
        if (enabled != other.enabled) {
            return false;
        }
        return true;
    }

}
